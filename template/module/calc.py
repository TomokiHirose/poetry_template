class Calc: 

    def __init__(self, a, b):
        """[summary]

        Args:
            a ([type]): [description]
            b ([type]): [description]
        """
        self.a = a
        self.b = b

    def add(self):
        """[summary]

        Returns:
            [type]: [description]
        """
        return self.a + self.b

    def dif(self):
        """[summary]

        Returns:
            [type]: [description]
        """
        return self.a - self.b

    def seki(self):
        """[summary]

        Returns:
            [type]: [description]
        """
        return self.a*self.b

    def shou(self):
        """[summary]

        Returns:
            [type]: [description]
        """
        return self.a/self.b